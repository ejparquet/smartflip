export const getExpensesByProject = (projectId: string): Expense[] => {
  return mockExpenses.filter((e) => e.projectId === projectId);
};

export const getExpensesByCategory = (projectId: string) => {
  const expenses = getExpensesByProject(projectId);
  const breakdown: Record<string, number> = {};
  
  expenses.forEach((expense) => {
    if (breakdown[expense.category]) {
      breakdown[expense.category] += expense.amount;
    } else {
      breakdown[expense.category] = expense.amount;
    }
  });
  
  return breakdown;
};

